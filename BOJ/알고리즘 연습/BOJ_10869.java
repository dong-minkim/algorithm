import java.util.Scanner;

public class BOJ_10869 {
	public static void main(String[] args) {
		int A,B;
		
		Scanner scan = new Scanner(System.in);
		A=scan.nextInt();
		B=scan.nextInt();
		
		System.out.println(A+B);
		System.out.println(A-B);
		System.out.println(A*B);
		System.out.println(A/B);
		System.out.println(A%B);
	}
}
